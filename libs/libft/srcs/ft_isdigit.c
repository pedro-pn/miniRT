/** This function checks whether c is a digit.
 * @param c The character to be checked.
 * @return A postive integer if c is a digit and 0 if not.
 * */
int	ft_isdigit(int c)
{
	if (c >= 48 && c <= 57)
		return (2048);
	return (0);
}
